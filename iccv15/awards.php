<p>PAMI-TC calls for nominations for two awards to be presented at ICCV 2015 in Santiago. Both these awards are normally made on a biennial basis, and are presented at ICCV.</p><br />

<p><span class="iccvsectionheader">PAMI Azriel Rosenfeld Lifetime Achievement Award</span></p>

<p>This award is given to researchers in Computer Vision who have made major contributions to the field over their career and who have influenced the field in an extraordinary way.</p>

<p>Previous winners of this award are listed at the following link - <a href="http://www.computer.org/portal/web/tcpami/Azriel-Rosenfeld-Life-Time-Achievement-Award">http://www.computer.org/portal/web/tcpami/Azriel-Rosenfeld-Life-Time-Achievement-Award</a></p>

<p><span class="iccvparagraphheader">Nominations</span></p>

<p>Nominations should consist of a statement of the nominee’s major contributions in research, education, administration, commercialization and service. Nominations should clearly state the influence the nominee has had in advancing the field of Computer Vision.</p>

