<p><span class="iccvsectionheader">PAMI Distinguished Researcher Award</span></p>

<p>This award (formerly called Significant Researcher Award) is awarded to candidates whose research contributions have significantly contributed to the progress of Computer Vision.</p>

<p>Previous winners of this award are listed at the following link - 
