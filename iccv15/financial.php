<strike>Young researcher support application
  form</strike><br />

<p>This application is for young researchers (within 5 years of
receiving their Ph.D.) in the computer vision community who may need
support to attend ICCV 2015. At this time we are unable to determine
the exact amount of support or reimbursement we are able to give as it
depends on the overall number of registrations and other elements of
the conference costs. So, our plan is to have you fill in this
application form and also pay for the full registration. Then, as the
conference date approaches, we will be in touch regarding the amount
(if any) of support we can provide. This will most likely come in the
form of a reimbursement for your conference fees, but it also may
require that you help during the conference, as a volunteer. (Note
that registration fee will be reimbursed to the credit card that was
used when registering. These are not travel grants.)</p>

<p>Only young researchers with accepted papers at the main conference
are eligible to apply.</p>

