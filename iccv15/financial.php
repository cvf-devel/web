<p>What you need to do as a student volunteer: work for 8 hours
(distributed in shifts) during the course of the conference. Work
involves transporting materials, attending to registrations at the
desk, running between various sessions to help the session chairs,
etc.</p>

<p>What you get in return as a student volunteer: your conference
registration fee is waived. (We ask that you register as normal and
once you have satisfied your volunteer duties, we will refund the
registration. Note that registration fee will be reimbursed to the
credit card that was used when registering. These are not travel
grants. So if your professor paid for registration, the fee will be
returned to your professor's card.)</p>

<p>Only current students are eligible to be student volunteers.</p>

<p>Applications received before October 15, 2015 will receive full
consideration, but applications will be accepted until all student
volunteer positions are filled.</p>

