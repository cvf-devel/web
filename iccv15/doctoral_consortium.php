<span class="iccvsectionheader">Overview</span>

<p>The ICCV Doctoral Consortium will provide a unique opportunity to
students who are close to finishing, or who have very recently
finished, their doctorate degree, to interact with renowned and
experienced researchers in the field, and discuss with them their
ongoing research and career plans. One senior member of the community
will be assigned as a mentor for each student based on similarity of
research interests. At the meeting, each student will discuss their
research and future plans with their mentor and other participants.</p>

