<span class="iccvsectionheader">Eligibility</span>

<p>All Ph.D. students in computer vision who will graduate within the
next 6-8 months, as well as those who have graduated in the past 6
months are eligible.</p>

<span class="iccvsectionheader">Submission Guidelines</span>

<p>Eligible students who would like to participate in the Doctoral
Consortium should submit an application via CMT at
<a href="https://cmt.research.microsoft.com/DCICCV2015/">https://cmt.research.microsoft.com/DCICCV2015/</a>. The application is due
on October 5, 2015 (9PM Pacific Time), and should include the following
information:</p>

<ol style="list-style-type: decimal">
<li>Is your university/college/department a US institution? (yes/no)</li>
<li>Expected graduation date</li>
<li>Names of 5 potential mentors</li>
<li>Title and author list of the poster that will be presented at the consortium, which may or may not be presented at ICCV as well</li>
<li>Research statement (1 page + unlimited references) summarizing the applicant's research and progress to date</li>
<li>Applicant's CV</li>
<li>Note from applicant's advisor confirming the date of graduation and stating the advisor's availability as a mentor.</li>
</ol>

<p>Items 3-7 should be uploaded as a single pdf file.<br />
All information is required. Incomplete applications will be rejected. Page limit on research statement is strict.</p>

<span class="iccvsectionheader">Review Process</span>

<p>If all applications cannot be supported, selection will be based on
the above material and the student's graduation date. Institutional
representation will be taken into account; it is unlikely that more
than two students from the same institution will be invited.</p>

<span class="iccvsectionheader">Travel Awards</span>

<p>Selected students will receive travel reimbursements for part of
their expenses. The amount is anticipated to be on the order of $700
per participant. Receipts will be required for processing
reimbursements. Details will posted.</p>

