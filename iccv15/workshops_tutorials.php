<h2 style="text-align:left;">Call for proposals for ICCV 2015 Workshops and Tutorials
<br/><font size="3"><span>Santiago, Chile, December 11-12 and 17-18, 2015</font><br/></h2>

<div class="paragraph" style="text-align:left;display:block;">
The 2015 International Conference on Computer Vision will host four days of workshops and tutorials prior to and following the main conference. We invite proposals from the community for both workshops and tutorials on any topic related to computer vision and its applications. Submitted proposals will be reviewed by a joint Workshops and Tutorial Program Committee.
<br><br>

Details on the content of each type of proposal are explained below. All proposals should be submitted as a single pdf document by email to
<a href="http://www.google.com/recaptcha/mailhide/d?k=01C4rFhzNBHOH8e2VBlkkiyw==&c=nCzg2tnlgZ7KOHepzcQwRgWYt-1G3NoGY6W07v1n2-ymto58ihvEJuouKgvc-n-1" target="_blank">iccv15-workshop-tutorial-chairs@pamitc.org</a> by <b>May 8, 2015</b>.
Decisions are expected to be announced by June 6, 2015 to allow organizers to take advantage of CVPR 2015 to hold an initial organizational meeting.
<br><br>
<br/>
<b>Workshops</b> are half-, one- or two-day meetings. Proposals on focused topics in core Computer Vision as well as cross-cutting themes with other scientific and application areas are welcome. We particularly encourage workshops on emerging research directions and applications, as well as challenges or competitions.
<br><br>

All <b>workshop</b> proposals should include at least the following information:
<ol>
<li>Title</li>
<li>Proposer's name, address, and email</li>
<li>Duration (half, 1-day, 2-day)</li>
<li>Abstract and justification</li>
<li>Tentative list of invited speakers if any</li>
<li>Structure of the workshop (e.g., invited talks with tentative list of speakers, contributed talks and/or posters with tentative program committee, panel sessions)</li>
<li>If the workshop will have peer-reviewed papers, proposers should submit a timeline. This timeline should be consistent with a workshop paper acceptance decision by October 1st and a camera-ready submission by <b>October 16th</b>. Since ICCV decisions will be announced on September 3rd 2015, workshop proposers could consider an earlier submission deadline for workshop papers not submitted to ICCV, and a later submission deadline for papers that are awaiting for ICCV decisions.</li>
<li>Intended audience and relation to recent ECCV/CVPR/ICCV workshops.</li>
 Is this a continuation of a workshop series?</li>
<li>Estimated attendance</li>
<li>Any plans for a special journal issue or a book.</li>
</ol> 

<br/>
<b>Tutorials</b> are usually half-day with the option of a full day course
for wider scope topics. A tutorial should be addressed to an audience at the level of
beginning graduate students or engineers in industry.
The topic should be of sufficient relevance with respect to the state
of the art and the course should rather be educating than just a
survey of techniques. Hands-on tutorials are highly encouraged.
<br/><br/>

All <b>tutorial</b> proposals should include:
<ol>
<li>Title</li>
<li>Proposer's name, address, and email</li>
<li>A course description in syllabus like form</li>
<li>Length of course (half day/full day)</li>
<li>Names of lecturers</li>
<li>Estimated attendance</li>
<li>Mention any previous CVPR or ICCV or ECCV tutorial topics on that matter</li>
<li>Whether PDF slides will be available for inclusion in the conference CDROM</li>
<li>A plan for a web site that will host the program and the course material</li>
</ol>

</div>


