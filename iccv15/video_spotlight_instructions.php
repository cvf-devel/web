<p>Authors of each paper accepted to ICCV 2015 (oral and poster) will submit a one-minute video spotlight that highlights and summarizes their ICCV 2015 paper.  These video spotlights will be independently available on an ICCV 2015 YouTube channel and collated into larger videos that provide an overview of each session of the conference.  Please read these instructions carefully to ensure that your video spotlight is formatted and submitted correctly.</p>

<p>The deadline for submitting your video spotlight is October 9, 2015 at 11:59pm Pacific Time.  This is NOT the same deadline as the full camera-ready paper.</p>

<p>FORMAT:</p>

<ul>
<li><p>Length: Maximum 60 seconds.  Everything after 60 seconds will be cut.</p></li>
<li><p>File Size: Maximum 100MB (104,857,600 bytes)</p></li>
<li><p>Size: 16:9 (1280x720) (per YouTube specifications)</p></li>
<li><p>Audio: Allowed. BUT NOTE: may not show on the concatenated videos playing at the conference, but will be preserved when uploaded to YouTube.  AAC or MP3 codec.</p></li>
<li><p>Frame Rate: 30Hz.  All other rates will be converted to 30Hz.</p></li>
<li><p>Format:  MP4 files with the H.264 codec.  We will make a best effort to convert other formats, but cannot guarantee they will work.</p></li>
<li><p>Content: Please keep legal restrictions in mind (e.g. do not use footage or images which you are not entitled to distribute beyond fair use). The videos will be on display in a public place to a wide range of nationalities, so avoid causing offense. We reserve the right to withdraw videos we believe to be outside these guidelines. If you suspect you may come close to the boundaries of acceptability, submit several days early and email us for approval.  All videos will be reviewed before releasing and collating.</p></li>
<li><p>Templates:  There is no template for the videos.  Authors are permitted to include a title screen for their paper.  However, do note that we will be prepending a 3 second title screen to each video that contains paper, author and session information.</p></li>
</ul>

<p>SUBMISSION: Your one-minute video spotlight is to be submitted as a single MP4 file using the "Presentation" page in CMT, as detailed in the steps below.   Your video spotlight must be submitted as an MP4 file.  (Note that although the "Presentation" submission page allows for other formats, any file format other than MP4 will be discarded).</p>

<ol type="a" class="body_ul">
	<li><p>To submit your video spotlight MP4 file, login to the CMT paper collection site:<br />
		<a href="https://cmt.research.microsoft.com/ICCV2015/">https://cmt.research.microsoft.com/ICCV2015/</a></p></li>
	<li><p>In the CMT "Author Console", your accepted paper(s) will appear in the "Camera-Ready | Presentations" column.  Click on the "Edit" link beneath the "Presentation" heading (next to your paper title).  DO NOT click on the "Edit" link next to the "Camera-Ready" heading or on the "Submit IEEE Copyright Form" - these links are for submitting your full, camera-ready paper as described in the camera-ready instructions.</p></li>
	<li><p>Similar to the full, camera-ready submission, the name of your video spotlight must be of the form XXXX_video.mp4 where XXXX is the four-digit paper ID (zero-padded if necessary). For example, if your paper ID is 24, the filename must be 0024_video.mp4.</p></li>
	<li><p>Use the "Browse" and "Upload" buttons on the "Presentation Details" page to find and upload your MP4 file of your video spotlight.</p></li>
	<li><p>You do not need to enter any "Presenters" for your video spotlight.</p></li>
	<li><p>Again, the deadline for submitting your one-minute video spotlight is October 9, 2015 at 11:59pm Pacific Time.</p></li>
</ol>


