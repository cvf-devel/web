<h2 style="text-align:left;">Call for Tutorials
<br/><font size="3">Due date: July 15, 2013</font><br/></h2>


<div class="paragraph" style="text-align:left;display:block;">
Proposals are invited for tutorials and short courses at the 2013 International Conference on Computer Vision in Sydney, Australia.  Short courses and tutorials will take place on December 1-2 and December 7-8, 2013, at the same venue as the main ICCV conference.
<br><br>

Short courses and tutorials are usually half-day, with the option of full day courses for wider scope topics. The audience is mostly graduate students. They should aim to address a specific topic in computer vision and provide a comprehensive overview of the topic, which must be relevant to the state of the art in computer vision.  Courses should be educational and include material that is fundamental to the topic, rather than just presenting a survey of techniques.
<br><br>

Proposal evaluation criteria will include the quality of the proposed plan, significance of the topic, and previous tutorials on the subject.
<br><br>

