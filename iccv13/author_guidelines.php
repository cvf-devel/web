<br><br>
<span class="iccvsectionheader"><a name="supplementary"></a>Supplementary Materials</span>
<br><br>
Authors may optionally upload supplementary material (up to 100MB) which may include:
<ul>
  <li>videos to showcase results/demo of the proposed approach/system,</li>
  <li>images and other results in addition to the ones in the paper,</li>
  <li>anonymized related submissions to ICCV or other conferences and journals, and</li>
  <li>appendices or technical reports containing extended proofs and mathematical derivations that are not essential to the understanding of the submitted paper.</li>
</ul>

ICCV encourages authors to submit videos using an MP4 codec such as DivX contained in an AVI. Also, please submit a README text file with each video specifying the exact codec used and a URL where the codec can be downloaded.
<br><br>
Authors should refer to the contents of the supplementary material appropriately in the paper. Note that reviewers will be encouraged to look at it, but are not obligated to do so. Please note that:
<ul>
  <li>All supplementary material must be self-contained in a single file for upload (i.e., a single pdf, or a zip file containing multiple items).</li>
  <li>The supplementary material directly supports the paper as submitted prior to the paper deadline. ONLY results generated by the algorithm/approach/system reported in the submitted version are allowed. Material based on improvements subsequent to the paper deadline is not allowed.</li>
  <li><span class="iccv-bold-red">(NEW)</span> <b>DO NOT</b> include links to external files or websites (e.g., youtube). Including such links is grounds for automatic rejection without review.</li>
  <li><b>DO NOT</b> submit a newer version of the paper as supplementary material. Including a newer version of the paper or any portion thereof (whether a single spelling/typo correction or a description of an improved algorithm/approach/system) is strictly forbidden.</li>
</ul>




