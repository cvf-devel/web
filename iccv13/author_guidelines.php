<span class="iccvsectionheader"><a name="faq"></a>Author FAQs</span>
<br><br>
<span class="iccvparagraphheader">About Submitting Papers</span><br>
<ul>
<li><span class="faq_q">Can we please have an extension on the deadline?</span><br>
<span class="faq_a"><b>NO.</b> No extensions will be given and any incomplete submission or a submission not meeting required criteria will be deleted.</span></li>

<li><span class="faq_q">The submission instructions ask me to enter only the domain name of my current institution in the "Conflict Domains" field but CMT’s instructions on the "Edit Conflict Domains" say otherwise. What should I do?</span><br>
<span class="faq_a">You should <b>ignore</b> the instructions on the "Edit Conflict Domains" page. Please <b>only</b> enter the domain name of your current institution. <b>NO</b> past institutions (where you worked or studied more than 12 months ago), <b>NO</b> domain names of co-author/collaborator institutions, <b>NO</b> domain names of email providers should be entered. The instructions on the "Edit Conflict Domains" page on CMT are common across all conferences hosted by CMT and cannot be customized for ICCV13. We ask that you ignore them and follow the ICCV13-specific instructions.</span></li>

<li><span class="faq_q">I entered my co-author domains as conflicts when I first logged on to CMT, but the paper submission instructions now say I should have only entered my institution’s domain. What should I do?</span><br>
<span class="faq_a">You should go back to your Author Console and just enter your institution’s domain, per ICCV13’s submission instructions. Your "Conflict Domains" field can be edited at any time during the submission period.</span></li>

<li><span class="faq_q">Since I am not entering the domains of my past collaborators, how will you determine which reviewers have a conflict of interest with my submission?</span><br>
<span class="faq_a">We are using DBLP to collect every reviewer’s co-authors in the past 3 years. Papers written by these co-authors will be flagged as having a conflict with the reviewer.</span></li>

<li><span class="faq_q">No Area Chair seems to have listed my paper’s subject area as primary. How should I choose my preferred AC?</span><br>
<span class="faq_a">You should consult <a href="ac_profiles.php">this webpage</a> to get complete information about our ACs. Since ACs can only specify one subject area as primary regardless of the breadth of their expertise, they may have a similar level of interest and expertise in their secondary subject areas. If your paper’s primary subject area does not appear as secondary either, we recommend you look at the ACs’ publications.</span></li>

<li><span class="faq_q">How will my paper’s Area Chair be determined if I do not select any preferred ACs?</span><br>
<span class="faq_a">We will use the Toronto Paper Matching System as an additional source of information for AC-paper assignments.</span></li>

<li><span class="faq_q">Can we get my quota increased for the size of paper submission from 20 MB to something higher?</span><br>
<span class="faq_a"><b>NO.</b> We have set hard limits of 20MB (PDF Only) for paper submission and 100MB (PDF or ZIP only) for supplementary materials for submissions for review. We are expecting many submissions and each reviewer is expected to review about 10 papers on average. Larger file downloads (and uploads) will burden the system and prevent reviewers from accessing papers quickly enough. Authors should consider adding high-resolution images as supplementary material. See the <a href="#supplementary">Supplementary Material</a> guidelines for more details.</span></li>
<li><span class="faq_q">How do I delete Supplementary Material from the CMT site?</span><br>
<span class="faq_a">After you log on to CMT, you will notice an "Update File" link next to the supplementary filename on the "Author Console". Click on that, and in the page that appears, you can click on the "Delete" button to remove the supplementary file. Note that you will not be able to delete the supplementary file after the supplementary file deadline.</span></li>
<li><span class="faq_q">Can we submit color images with our papers for review?</span><br>
<span class="faq_a"><b>YES.</b> Reviewers will download the exact pdf file of the paper you submitted, so they can see the color images on the screen. Nevertheless, many reviewers like to review submissions in hardcopy and may not have access to a high-end color printer. We suggest that you print your paper on a monochrome printer to verify that important information is not lost after monochrome printing. You may also wish to add a comment in your submission requesting the reviewers to view it in color, on a screen.</span></li>
<li><span class="faq_q">What is the ICCV 2013 policy on DUAL SUBMISSIONS?</span><br>
<span class="faq_a">See the <a href="#policies">Policies</a> section of this webpage.</span></li>
<li><span class="faq_q">Does a Technical Report (departmental, arXiv, etc.) with publication available online count as a prior publication?</span><br>
<span class="faq_a"><b>NO.</b> See the <a href="#policies">Policies</a> section of this webpage.</span></li>
<li><span class="faq_q">Does a presentation at a departmental seminar during the review period violate the anonymity standard or other ICCV 2013 policy?</span><br>
<span class="faq_a"><b>NO.</b> See the <a href="#policies">Policies</a> section of this webpage.</span></li>
</ul>
<br>

<span class="iccvparagraphheader">About the Review Process</span><br>
<ul>
<li><span class="faq_q">Is the ICCV 2013 Review Process CONFIDENTIAL?</span><br>
<span class="faq_a"><b>YES,</b> ICCV 2013 reviewing is considered confidential. All reviewers are required to treat every manuscript they review as a confidential document and not to share or distribute materials under review for any reason other than to facilitate the reviewing of the submitted work. Reviewers are obligated to remove or destroy them after review. Accepted papers will be published (with appropriate copyrights) with a date of around December 3, 2013. Please make sure to discuss this issue with your legal advisors as it pertains to public disclosure of the contents of the papers submitted.</span></li>
<li><span class="faq_q">Are ICCV 2013 Reviews Double BLIND or Single BLIND?</span><br>
<span class="faq_a">ICCV reviewing is <b>Double BLIND</b>. Be sure to read the <a href="#policies">Policies</a> section of this webpage to avoid unintentional violations of the double-blind policy when writing your paper.</span></li>

<li><span class="faq_q">Is it guaranteed that my paper will be handled by one of my preferred ACs?</span><br>
<span class="faq_a"><b>NO.</b> While we will do our best to satisfy author preferences, each AC can only handle a limited number of papers. In addition, ACs have a choice as well: ACs will initially be assigned 60-80 papers (based on author preferences and the Toronto Paper Matching System), and then they will indicate which of these papers match their interests and expertise most closely. The final AC-paper assignment will be based on this input from ACs, subject to load-balancing constraints.</span></li>
</ul>
<br>


<span class="iccvparagraphheader">About Publications</span><br>
<ul>
<li><span class="faq_q">Since the ICCV 2013 proceedings are electronic/digital, why do we need to pay a fee if our paper is accepted? Where are these funds used?</span><br>
<span class="faq_a">Every submitted paper increases the workload of reviewers and area chairs, and thus the cost of the reviewing and publication process. The fee helps defray these costs and is considered a better alternative to increasing the registration fees of every attendee. Note that this fee is normally covered by grants/contracts.</span></li>
</ul>


