<span class="iccvsectionheader"><a name="policies"></a>Policies</span>
<br><br>

<span class="iccvparagraphheader">Author Responsibilities:</span> By submitting a manuscript to ICCV, authors agree
<ul>
  <li>to abide by all ICCV policies regarding <a href="#doubleblind">double-blind reviewing</a>, <a href="#dual">double submissions</a> and <a href="#conflicts">conflicts of interest</a>.</li>
  <li><span class="iccv-bold-red">(NEW)</span> to pay a publication fee of US$200 if the paper is accepted; this fee will be charged at the time of conference registration.</li>
  <li>that if the paper is accepted, at least one of the authors will register for the conference and present the paper there.</li>
</ul>
<br>
<span class="iccvparagraphheader"><a name="doubleblind"></a>Double blind review:</span> ICCV reviewing is double blind, in that authors do not know the names of the area chair/reviewers of their papers, and area chairs/reviewers do not know the names of the authors. Please read Section 1.6 of the example paper <a target="_blank" href="http://www.pamitc.org/iccv13/files/egpaper_for_review.pdf">egpaper_for_review.pdf</a> for detailed instructions on how to preserve anonymity. Avoid providing information that may identify the authors in the acknowledgments (e.g., co-workers and grant IDs) and in the supplemental material (e.g., titles in the movies, or attached papers). Avoid providing links to websites that identify the authors. Violation of these guidelines can lead to rejection.
<br><br>

<span class="iccvparagraphheader"><a name="dual"></a>Dual/Double Submissions:</span> By submitting a manuscript to ICCV, the authors guarantee that
<ul>
  <li>the manuscript has not been previously published or accepted for publication in substantially similar form in an archival peer-reviewed forum.</li>
  <li>no paper which contains significant overlap with the contributions of this paper is under review at the moment of submission or will be submitted during the ICCV 2013 review period to another conference, workshop, or journal (April 12, 2013 to September 2, 2013).</li>
  <li>they did not submit substantially similar submissions to ICCV 2013.</li>
</ul>

Violation of any of these conditions will lead to rejection. If there are papers that may appear to violate any of above conditions, it is the authors' responsibility to (1) cite these papers (preserving anonymity as described in Section 1.6 of the example paper <a target="_blank" href="http://www.pamitc.org/iccv13/files/egpaper_for_review.pdf">egpaper_for_review.pdf</a>), (2) argue in the body of your paper why the ICCV submission is non trivially different from these concurrent submissions, and (3) include anonymized versions of those submissions as supplemental material.
<br><br>
Note that a Technical Report (departmental, <a target="_blank" href="http://arxiv.org/">arxiv.org</a>, etc.) that is disseminated without any form of direct peer review is NOT considered a publication. Likewise, mention of the work under review in a presentation is NOT considered a violation. For further information, please refer to <a target="_blank" href="http://www.ieee.org/publications_standards/publications/rights/Section_822F.html">Section 8.2.4.F</a> of the IEEE PSPB Operations Manual.
<br><br>

<span class="iccvparagraphheader"><a name="conflicts"></a><span class="iccv-bold-red">(NEW)</span> Conflicts of Interest:</span>  It is the primary author's responsibility to ensure that
<ul>
  <li>the domain name(s) entered on CMT is/are valid and represent his/her place(s) of employment or study in the last 12 months.</li>
  <li>no other domain names are included.</li>
  <li>the chosen Preferred Area Chairs do not have a conflict of interest with	 any of the submission’s authors (i.e., are currently at the same institution or have had a co-author/supervisor/supervisee relationship in the last 3 years).</li>
</ul>

Violations of these rules may lead to rejection without review. 
