<span class="iccv-bold-red">Please read the following submission instructions carefully, as some steps are new or have changed for ICCV 2013.</span>  Your submission will not be complete unless all of the steps below are followed. Instructions on <a href="#formatting">formatting your submission</a>, a description of the general <a href="#policies">ICCV 2013 policies</a>, and some <a href="#faq">frequently-asked questions</a> can be found at the bottom of this page.

<br><br>
<span class="iccvparagraphheader">Step 1: Create a user account on the CMT paper submission site</span>
<ul>
  <li>Please follow <a target="_blank" href="https://cmt.research.microsoft.com/ICCV2013/Public/SignUp.aspx">this link</a> to sign up as a new user on CMT.</li>
  <li>You can edit your contact information at any time (see item near the top right in the submission site). Don't forget to click the "Update" button to save the edited information. If you wish to change the contact email address, you can modify it via the "Change your Email" box.</li>
  <li><b>Note:</b> If you have already registered as an ICCV 2013 reviewer on CMT you can ignore this step.</li>
  <li><b>Note:</b> If you have generated an account and have forgotten your password, just click on "Reset your password". Instructions will be emailed to you.</li>
</ul>


<br>
<span class="iccvparagraphheader">Step 2: <span class="iccv-bold-red">(NEW)</span> Enter the domain of ONLY your current institution</span>
<ul>
  <li>The first time you <a target="_blank" href="https://cmt.research.microsoft.com/ICCV2013/Default.aspx">log on to CMT</a> you will be asked to enter your "Conflict Domains".</li>
  <li><span class="iccv-bold-red">(NEW)</span> Please <b>ignore</b> CMT's instructions on the "Edit Conflict Domains" page about which domains to enter.</li>
  <li><span class="iccv-bold-red">(NEW)</span> Please only enter the domain of the academic department or institution where you <b>currently</b> work or study (example: <i>cs.toronto.edu</i>). If you worked or studied at more than one institution <b>in the past year</b>, please enter the domain of each institution separated by semicolons (example: <i>cs.washington.edu; microsoft.com</i>).</li>
  <li>If your institution is a university, please enter the domain of your academic department rather than the whole university (i.e., enter <i>cs.washington.edu</i> rather than <i>washington.edu)</i></li>
  <li><span class="iccv-bold-red">(NEW)</span> <b>DO NOT</b> enter the institution domain names of your current or past co-authors: only your institution should be entered. ICCV 2013 will use a different mechanism to identify collaborator conflicts which does not rely on the information you enter on CMT.</li>
  <li><span class="iccv-bold-red">(NEW)</span> <b>DO NOT</b> enter the domain of your email providers such as gmail.com, yahoo.com, hotmail.com, 163.com as your institution domain.</li>
</ul>


<br>
<span class="iccvparagraphheader">Step 3: Enter a title and abstract</span>
<br>
Your title can be up to 256 characters in length. The maximum size of the abstract is 4000 characters.
<br>


<br>
<span class="iccvparagraphheader">Step 4: Add your co-authors</span>
<br>
If your submission has co-authors, you will need to associate them with your submission:
<ul>
  <li>If your co-authors already have an account on CMT, please make sure you enter the email address that corresponds exactly to their account name. This will ensure that they can see your submission when they log onto CMT.</li>
  <li>If the email address you enter does not correspond to an existing CMT account for ICCV 2013, you will be asked to enter your co-author’s information (name, institution, etc).</li>
</ul>


<br>
<span class="iccvparagraphheader">Step 5: <span class="iccv-bold-red">(NEW)</span> Select one TOP-LEVEL primary subject area for your paper</span>
<ul>
  <li>Select one TOP-LEVEL primary subject area for your paper.</li>
  <li><span class="iccv-bold-red">(NEW)</span> Subject areas are organized hierarchically in two levels. Please choose a <b>top-level</b> subject area as the primary area of your paper. Pay extra attention to this choice, as it is critical in allowing us to properly assign papers to reviewers.</li>
</ul>


<br>
<span class="iccvparagraphheader">Step 6: Select up to five secondary subject areas</span>
<ul>
  <li>You may select any combination of top-level and lower-level subject areas. Pay extra attention to these choices, as they are critical in allowing us to properly assign papers to reviewers.</li>
  <li>Your primary subject area cannot be selected as a secondary subject area; if you select it, the system will not allow you to save. It is possible, however, to select secondary subject areas that lie below your primary subject area in the hierarchy. For example, if you have chosen "3D computer vision" for your primary subject area, you cannot choose "3D computer vision" as a secondary subject area but you can choose "Stereo correspondence".</li>
</ul>


<br>
<span class="iccvparagraphheader">Step 7: <span class="iccv-bold-red">(NEW)</span> Optionally select up to five Preferred Area Chairs (ACs) for handling your paper</span>
<ul>
  <li>If you do select preferred ACs, your choices will be used to determine who will shepherd your paper through the reviewing process.</li>
  <li>You may consult <a target="_blank" href="ac_profiles.php">this webpage</a> for information about the ICCV 2013 Area Chairs: their names, websites, preferred subject areas, Google Scholar profiles, and publications on DBLP.</li>
  <li>Choosing Preferred ACs is <b>optional</b>. If you do not know which ACs to select, you may keep the default option ("No preference").</li>
  <li>Keep in mind that each AC can only handle a limited number of papers; <b>the more preferred ACs you specify the more likely it is that one of your choices will be available to handle your paper.</b></li>
  <li>The preferred AC drop-down list includes only the AC’s primary subject area. Since ACs can only specify one subject area as primary regardless of the breadth of their expertise, it is possible that your paper’s primary subject area is not included on this list, or that very few ACs have it as primary. You should consult <a target="_blank" href="ac_profiles.php">this webpage</a> to view the ACs’ secondary subject areas and their publication profiles to help you decide whom to choose.</li>
  <li><b>DO NOT</b> choose the same AC more than once.</li>
  <li><b>DO NOT</b> choose ACs who are in conflict of interest with either you or your submission's co-authors. Please see the <a href="#conflicts">ICCV 2013 conflict of interest policy</a> for more details.</li>
</ul>


<br>
<span class="iccvparagraphheader">Step 8: Include your paper ID</span>
<br>
Once you have registered your paper (i.e. title, subject areas, area chairs, etc), you will be assigned a paper ID by CMT. Insert this ID into the latex or word template before generating the PDF of your paper for submission.
<br>


<br>
<span class="iccvparagraphheader">Step 9: Upload your paper's PDF by April 12, 2013 (7:00 PM PDT [UTC-7:00])</span>
<ul>
  <li>Paper formatting instructions can be found <a href="#formatting">here</a>.</li>
  <li>Only PDF files are accepted (maximum size <b>20MB</b>).</li>
  <li>Make sure that your paper’s ID appears in the PDF.</li>
</ul>


<br>
<span class="iccvparagraphheader">Step 10: Optionally upload a supplementary materials file by April 17, 2013 (7:00 PM PDT [UTC-7:00])</span>
<ul>
  <li>The ICCV 2013 policy on supplementary materials can be found <a href="#supplementary">here</a>.</li>
  <li><span class="iccv-bold-red">(NEW)</span> Only one PDF or ZIP file is accepted with a maximum size of <b>100MB</b>.</li>
  <li>You can update this file by uploading a new one (the old one will be deleted and replaced).</li>
  <li>The paper for review (PDF only) must be uploaded before the supplementary material (PDF or ZIP only) can be uploaded.</li>
</ul>








