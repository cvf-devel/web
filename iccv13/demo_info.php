allows, the time of submission of the complete application will be
used for selection. The conference also reserves the right to select
demonstrations based on the degree of appropriateness for ICCV.
<br/><br/>
It is also important to note that, nonetheless demonstrations can
be made with the help of industrial partners, commercial products
should be presented as part of the ICCV exhibits, rather than
demonstrations.
