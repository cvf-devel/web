ICCV is the premier international Computer Vision event comprising
the main conference and several co-located workshops and short
courses. ICCV 2013 will host demonstrations whose purpose is to
give researchers the opportunity to showcase live demonstrations
of their contributions.
<br/><br/>
Members of the computer vision community are invited to submit
expressions of interest for demonstrations at ICCV 2013. These do
not need not be limited only to papers that are to be presented
at ICCV 2013. Researchers are encouraged to demonstrate the
ectiveness of methods described in papers presented in the past
conferences and in other venues, so long as they fall within the
broad context of ICCV.
