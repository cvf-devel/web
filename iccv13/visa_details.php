<span class="iccvparagraphheader">Business (Short Stay) visa (Subclass 456) — paper-based applications</span><br/>
All other passport holders need to submit a paper-based application for a Business (Short Stay) visa (Subclass 456). 
<br/><br/>
More information on this visa is available on the website. <b>See:</b> <a target="_blank" href="http://www.immi.gov.au/skilled/business/456/">www.immi.gov.au/skilled/business/456/</a>
This visa application can also include family members wanting to travel with you to Australia such as spouse and dependent children. 
<br/><br/><br/>
<span class="iccvsectionheader">Things to consider before lodging an application</span><br/>
<br/>
<span class="iccvparagraphheader">Health and character requirements</span><br/>
All visa applicants must be assessed against Australia’s health and character requirements. These requirements are designed to protect the safety and security of the Australian community. 
<br/><br/>
<span class="iccvparagraphheader">Health requirements</span><br/>
As part of the visa application process you may be required to undertake a medical examination, chest x-ray and/or other health checks. 
The department will advise you of the applicable health checks (if any), based on your individual circumstances. 
Information on which health checks may apply to you is available on the website. <b>See:</b> <a target="_blank" href="http://www.immi.gov.au/allforms/pdf/1163i.pdf">www.immi.gov.au/allforms/pdf/1163i.pdf</a>
<br/><br/>
<span class="iccvparagraphheader">Character requirements</span><br/>
As part of the visa application process, you must show that you are of good character to pass Australia’s character test. Information on the character test is available on the website. <b>See:</b> <a target="_blank" href="http://www.immi.gov.au/media/fact-sheets/79character.htm">www.immi.gov.au/media/fact-sheets/79character.htm</a>
<br/><br/>
If you are unsure whether you can pass Australia’s character test because of prior criminal convictions you should lodge a paper-based application at the Australian visa office nearest to your current place of residence, at least four months prior to the event start date. 
Contact information for Australian visa offices is available on the website. <b>See:</b> <a target="_blank" href="http://www.immi.gov.au/contacts/overseas/index.htm">www.immi.gov.au/contacts/overseas/index.htm</a>
<br/><br/>
<span class="iccvparagraphheader">Travel sanctions</span><br/>
For information on countries subject to travel sanctions, please refer to the Department of Foreign Affairs and Trade’s. Nationals of these countries may not be permitted to travel to and enter Australia. <b>See:</b> <a target="_blank" href="http://www.immi.gov.au/contacts/overseas/index.htm">www.dfat.gov.au/un/unsc_sanctions/index.html</a>
<br/><br/>

<span class="iccvparagraphheader">Biometrics Program</span><br/>
From December 2010, Biometrics will need to be collected at an Australian Visa Application Centre for visitors lodging a paper-based visa application from the countries listed at the department’s website at <a target="_blank" href="http://www.immi.gov.au/contacts/overseas/index.htm">http://www.immi.gov.au/allforms/biometrics/offshore/countries.htm</a>
<br/><br/>
<span class="iccvparagraphheader">Invitation and registration to attend the event</span><br/>
Although this is a separate process to lodging your visa application, when assessing whether you meet the criteria for the grant of a visa the department may take into account whether you are registered, or were invited, to attend the event. 
<br/><br/>
Confirmation of registration, or a copy of your letter of invitation, should accompany paper-based visa applications. 
<br/><br/>
<span class="iccvparagraphheader">Cut-off date for lodging paper based applications</span><br/>
<span class="iccv-bold-red">All visitors should apply for a visa <u>at least two months</u> prior to the intended date of departure</span>, and four months prior for people with character or health concerns. 
<br/><br/><br/>
<span class="iccvsectionheader">Obligations of visa holders</span><br/>
On arrival at Australia’s border you must identify yourself, present your travel documents, and complete an Incoming Passenger Card (IPC). The IPC requires visitors to declare their health status and any prior criminal convictions. 
<br/><br/>
If you do not meet immigration clearance requirements, you may be refused entry to Australia. 
More information on immigration clearance processes at Australia’s borders is available on the website. <b>See:</b> <a target="_blank" href="http://www.immi.gov.au/contacts/overseas/index.htm">www.immi.gov.au/managing-australias-borders/border-security/air/airport.htm</a>
<br/><br/>
You must comply with your visa validity and visa conditions while in Australia. When you receive your Australian visa you are issued with a visa grant notice that explains the conditions of the visa including period of validity and entry requirements. You are advised to carry this visa grant notice for your own reference. 
<br/><br/>
If you breach these conditions, your visa may be cancelled. 
<br/><br/>
If your visa expires while you are still in Australia, you become an unlawful non-citizen. Unlawful non-citizens are liable to be detained and removed from Australia.

