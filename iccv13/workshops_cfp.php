<h2 style="text-align:left;">Call for Workshops
<br/><font size="3"><span>Due date: May 3, 2013</font><br/></h2>

<div class="paragraph" style="text-align:left;display:block;">  Proposals for workshops are solicited for the 2013 IEEE International Conference on Computer Vision (ICCV 2013) in Sydney, Australia. Workshops will take place on December 1-2 and December 7-8, 2013, at the same venue as the main ICCV conference.
<br><br>

Appropriate subjects for workshops include topics from computer vision as well as related fields such as machine learning, computer graphics and human-computer interaction. We encourage submission of workshop proposals that are concerned with ideas/techniques/problems that are relatively new in computer vision. 
<br><br>

Proposals that emphasize formats and/or programs that are unique to workshops, such as a large proportion of invited speakers or being based on a competition, are specifically encouraged. Such proposals will be favoured over those that offer a traditional conference-type format.
<br><br>

The proposal should include the following information:
<br><br>

<ol>
<li>Title, topic and sample call for papers;</li>
<li>Justification of the workshop, including the relationship to the previous ones, the novelty of the proposed workshop, and the reason why the proposed workshop may interest the community.</li>
<li>Names and contacts of the organizers;</li>
<li> Tentative program committee, invited speakers if any;</li>
<li>Estimated attendance;</li>
<li> Estimated number of submissions and acceptance rate;</li>
<li> Estimated breakdown among oral, poster presentations, and invited talks;</li>
<li> Proposed length of the workshop, and date restrictions</li>
<li> Special space and equipment request if any.</li>
</ol> 

