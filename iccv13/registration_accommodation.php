<span class="iccvparagraphheader">BOOKING DEADLINE: 1 November 2013</span><br/>
You are encouraged to book your accommodation through the Conference Secretariat to ensure that you receive competitive rates.  You can add a hotel booking to your registration at any date, please contact the Secretariat. 
<br/><br/>
<span class="iccvparagraphheader">Deposits</span><br/>
Accommodation will not be confirmed until a deposit of one night’s tariff is received.  This deposit will be forwarded to your hotel and will be credited to your room account.  Delegates must settle the balance of their account with the hotel concerned upon check out.  If accommodation deposits are not received before <b>1 November 2013</b> your room will be released back to the hotel.  The Conference secretariat does not take responsibility for accommodation if deposits are not received by this date.

<br/><br/>
<span class="iccvparagraphheader">Deadline for accommodation bookings</span><br/>
The Secretariat has an obligation to return all unsold rooms to the hotels/apartments on <b>1 November 2013</b>, therefore accommodation bookings and rates reserved after that date cannot be guaranteed.  The rates quoted are only valid for bookings made prior to <b>1 November 2013</b>. 
<br/><br/>
Late bookings can be made through the Conference secretariat after <b>1 November 2013</b>, but are subject to availability and credit card details are required to secure the reservation.  One night’s accommodation tariff will be deducted from the supplied credit card, by the hotel, if the reservation is cancelled or the delegate fails to arrive on the specified date. 
<br/><br/>
<span class="iccvparagraphheader">Cancellations - No accommodation deposits will be refunded on or after <b>1 November 2013</b>.</span><br/>
Delegates who confirm attendance and provide credit card details but do not advise of cancellation before the 30 day period of Conference commencement will incur charges for the 'no-show' rate, as penalised by the hotel. This charge is calculated as the room rate per night multiplied by the dates booked and secured.  Please ensure that accommodation dates are correct at time of booking.
<br/><br/>
<span class="iccvparagraphheader">Early arrival</span><br/>
Some international flights arrive in Sydney in the very early morning. As check in is not until 3.00pm at most hotels, it is possible that your room may not be ready for immediate occupation on your early arrival. Whilst hotels will do everything possible to accommodate early arrivals, if you wish to be guaranteed immediate occupation you can pay an extra day’s tariff for the night before you are due to arrive.  If you wish to take this precaution, please advise the Secretariat on the registration form so that we can notify the hotel that your room is pre-registered.
<br/><br/>
<span class="iccvparagraphheader">Late arrival</span><br/>
Please indicate if you will arrive at your hotel after 6.00pm.  Failure to do so might result in your room being released.
<br/><br/>
<span class="iccvparagraphheader">Change of booking</span><br/>
Any change in booking must be made in writing to the Conference Secretariat and NOT directly to the hotel. 
<br/><br/>
<span class="iccvparagraphheader">Group accommodation bookings</span><br/>
The Conference Secretariat offers organisations the opportunity to contract ten or more rooms for the Conference.  This requires a contract to be signed by ICMS Australasia and your organisation to block the group reservation. 
