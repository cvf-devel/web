<span class="iccvsectionheader"><a name="reviewing"></a>ICCV 2013 Reviewer Guidelines</span>
<br><br>


<span class="iccvparagraphheader">Start Early</span>
<br><br>
Based on the published schedule, you will have four weeks to complete the pre-rebuttal review form; please do not leave this for the last few days before the <b>June 9 deadline</b>. Adhering to this deadline is extremely important since all reviews of all papers must be submitted before we can move to the paper discussion phase, where your reviews will be visible to your fellow reviewers and your Area Chair.
<br><br>
Similarly, you will have a week after the author rebuttals to submit your final recommendation by <b>July 21</b>. Again, adhering this deadline is extremely important because Area Chairs will have a lot of work to do after that point.
<br><br>

<span class="iccvparagraphheader">What to Look For</span>
<br><br>
Look for what's good or stimulating in the paper. Minor flaws can be corrected and shouldn't be a reason for rejection. Each paper that is accepted should, however, be technically sound and make a substantial contribution to the field.
<br><br>

<span class="iccvparagraphheader">Blind Reviews</span>
<br><br>
Blind reviewing is an essential part of ICCV reviewing. Authors were asked to take reasonable efforts to hide their identities, including not listing their names or affiliations and omitting acknowledgments. This information will of course be included in the published version. Reviewers should also make all efforts to keep their identity invisible to the authors. Don't say, "you should have cited my paper from 2006!"
<br><br>
Note that posting a paper online or giving a public talk does not violate the <a href="http://www.pamitc.org/iccv13/author_guidelines.php#doubleblind">double-blind policy</a>.  Reviewers must not seek the identity of the authors; authors must not bias the review process by suggesting their identities. 
<br><br>

<span class="iccvparagraphheader">Be Specific</span>
<br><br>
Please be specific and detailed in your reviews. In the discussion of related work and references, simply saying "this is well known" or "this has been common practice in the industry for years" is not sufficient: cite specific publications or public disclosures of techniques. The comment fields are the most important of the review. Your discussion, sometimes more than any scores, will help the authors, fellow reviewers and Area Chairs understand the basis of your opinion. 
<br><br>
Your reviews will be returned to the authors and they will be given a chance to respond in their rebuttal. It is therefore important to help authors understand which comments in your review are most important to address in their response and which are less so.
<br><br>
You should include any specific feedback on ways the authors can improve their papers. For more suggestions on writing your reviews, read the section below on <a href="#writingtechreviews">Writing Technical Reviews</a>.
<br><br>

<span class="iccvparagraphheader">Be proactive: participate in discussions & respond promptly to Area Chair requests</span>
<br><br>
Your task as a reviewer does <b>NOT</b> end when you submit the pre-rebuttal part of your review on June 9: reviewers are an integral part of the decision making and should be ready to provide additional input when necessary.
<br><br>
Donâ€™t expect the Area Chair to initiate discussion on papers. When the discussion period begins, your fellow reviewersâ€™ reviews will be visible to you by default. Take a look at what the other reviewers said right away and discuss on the CMT bulletin board.  Such discussions <b>must</b> take place in cases of disparate recommendations; disagreement on technical or subjective grounds; errors in the reviews (yours or othersâ€™); or new evidence (e.g. important missing related work or technical errors caught by a subset of the reviewers). 
<br><br>
If an Area Chair makes a request on the bulletin board, please respond promptly. Each Area Chair is responsible for dozens of papers and thus has a very significant workload; please help make their task easier by responding promptly when they make a request.
<br><br>

<span class="iccvparagraphheader">When You're Done</span>
<br><br>
When you have finished with your review, you should destroy any paper manuscript and/or supporting material you received. See the <a href="#ethics">Ethics guidelines</a> below.
<br><br>

