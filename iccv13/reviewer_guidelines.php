<span class="iccvsectionheader"><a name="ethics"></a>Ethics for Reviewing Papers</span>
<br><br>

<span class="iccvparagraphheader">1. Protect Ideas</span><br></br>
As a reviewer for ICCV, you have the responsibility to protect the confidentiality of the ideas represented in the papers you review. ICCV submissions are not published documents. The work is considered new or proprietary by the authors; otherwise they would not have submitted it. Of course, their intent is to ultimately publish to the world, but most of the submitted papers will not appear in the ICCV proceedings. Thus, it is likely that the paper you have in your hands will be refined further and submitted to some other journal or conference, or even to CVPR next year. Sometimes the work is still considered confidential by the author's employers. These organizations do not consider sending a paper to ICCV for review to constitute a public disclosure. Protection of the ideas in the papers you receive means: 
<ul>
<li>Do not show the paper to anyone else, including colleagues or students, unless you have asked them to write a review, or to help with your review.</li>
<li>Do not show any results or videos/images or any of the supplementary material to non-reviewers.</li>
<li>Do not use ideas from papers you review to develop new ones.</li>
<li>After the review process, destroy all copies of papers and videos that are not returned to the senior reviewer and erase any implementations you have written to evaluate the ideas in the papers, as well as any results of those implementations.</li>
</ul>


<span class="iccvparagraphheader"><a name="conflict"></a>2. Avoid Conflict of Interest</span><br></br>

As a reviewer of a ICCV paper, you have a certain power over the reviewing process. It is important for you to avoid any conflict of interest. Even though you would, of course, act impartially on any paper, there should be absolutely no question about the impartiality of review. Thus, if you are assigned a paper where your review would create a possible conflict of interest, you should return the paper and not submit a review. Conflicts of interest include (but are not limited to) situations in which: 
<ul>
<li>You work at the same institution as one of the authors.</li>
<li>You have been directly involved in the work and will be receiving credit in some way. If you're a member of the author's thesis committee, and the paper is about his or her thesis work, then you were involved.   </li>
<li>You suspect that others might see a conflict of interest in your involvement. For example, even though Microsoft Research in Seattle and Beijing are in some ways more distant than Berkeley and MIT, there is likely to be a perception that they are "both Microsoft," so folks from one should not review papers from the other.   </li>
<li>You have collaborated with one of the authors in the past three years (more or less). Collaboration is usually defined as having written a paper or grant proposal together, although you should use your judgment.   </li>
<li>You were the MS/PhD advisor of one of the authors or the MS/PhD advisee of one of the authors. Funding agencies typically consider advisees to represent a lifetime conflict of interest. ICCV has traditionally been more flexible than this, but you should think carefully before reviewing a paper you know to be written by a former advisee.</li>
</ul>
The blind reviewing process will help hide the authorship of many papers, and senior reviewers will try hard to avoid conflicts. But if you recognize the work or the author and feel it could present a conflict of interest, send the paper back to the Area Chair as soon as possible so he or she can find someone else to review it.
<br><br>

<span class="iccvparagraphheader">3. Be Professional</span><br></br>

Belittling or sarcastic comments have no place in the reviewing process.  The most valuable comments in a review are those that help the authors understand the shortcomings of their work and how they might improve it.  Write a courteous, informative, incisive, and helpful review that you would be proud to add your name to (were it not anonymous).  

