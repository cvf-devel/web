Thank you for agreeing to review for IEEE ICCV 2013. The quality of the conference depends to a large extent on the willingness of reviewers like you to provide thoughtful, high-quality reviews. By agreeing to review papers for ICCV 2013, you commit to
<ul>
  <li>review your assigned papers by the deadline dates</li>
  <li>assess the author rebuttals</li>
  <li>discuss the papers with Area Chairs and your fellow reviewers in a timely manner</li>
</ul>
All these duties will take place during the period of <b>May 13</b> to <b>July 21, 2013</b>. We will strive to limit the reviewing load to about 6 papers for student reviewers and 10 papers for everyone else. <b>Please read the following instructions carefully, as some reviewing duties are new or have changed for ICCV 2013</b>.
