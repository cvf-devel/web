CVPR has a strong tradition of hosting exhibits of advanced computer vision technology, products, and publications.  Our exhibit tables are strategically-placed to foster interaction between the exhibitors and conference attendees, ensuring maximum exposure and interaction. We particularly encourage companies and other commercial entities or laboratories to participate in showcasing their products.
<br><br>

