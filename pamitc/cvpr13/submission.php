            <tr><td>
              <ul>
                  <li>Before the paper deadline, Area Chairs were invited and accepted the role of working with the Program Chairs to provide best possible reviews to authors. A Review Committee was also formed.</li>
                  <li><b><span style="text-decoration:line-through">Nov 15, 2012 - Paper Deadline</span></b>: NO Extensions will be given and any incomplete submission or a submission not meeting required criteria will be deleted.</span></li>
                  <li><b><span style="text-decoration:line-through">Nov 22, 2012 - Supplemental Material Deadline</span></b>: Supplemental material must be uploaded by this date. No new algorithms or methods can be introduced in the supplemental materials.</span></li>
                  <li>Program chairs assign papers to area chairs.  The area chairs are divided into four panels of about equal sizes. Each area chair is also paired with a "buddy". Papers are assigned to each area chair such that these papers have no conflict with his/her "buddy" or panel. The buddy/panel system is designed to allow each area chair to safely discuss papers with his/her "buddy" or, if necessary, any panel colleague.</li>
                  <li>Area chairs recommend at least 5 reviewers for each paper assigned to them. For each paper, there can be no more than two suggested reviewers from the same organization.</li>
                  <li>Program chairs work with area chairs to ensure reviewers are selected from the area chairs' lists of suggested reviewers.</li>
                  <li>Each paper is assigned to 3 reviewers based on area chair recommendations, conflict information, and quotas.</li>
                  <li>Area Chairs start checking on the reviews of their assigned papers.</li>
                  <li><b><span style="text-decoration:line-through">Jan 21, 2013 - Reviews are DUE</span></b>: Area chairs are expected to shepherd papers closely. They are expected to monitor the reviews and contact individual reviewers (anonymously through CMT) for clarification if necessary. They are also expected to initiate anonymized discussions in cases where scores are highly divergent (max - min > 2 levels). Once the reviews are finalized, area chairs are asked to rate each review for each paper.</li>
                  <li><b>Feb 03, 2013 - Initial Reviews to Authors</b>: Reviews are made known to authors.</li>
                  <li><b>Feb 08, 2013 - Rebuttal Due</b>: Authors can submit rebuttals if they so desire (not required). There is a limit of 4000 characters. Authors can withdraw papers from further consideration by deleting the paper from their console.</li>
                  <li>Prior to the area chair meeting, each area chair writes consolidation reports in consultation with his/her "buddy", and prepares list of papers that need additional consideration, discussion, and/or AC reviews at the AC meeting.</li>
                  <li><b>Feb 23-24, 2013 - Area Chairs Meeting</b>: During the area chair meeting there will be "buddy" and panel meetings to make final decisions on papers.</li>
                  <li><b>Feb 24, 2013 - Final decision to authors</b>: Decisions are made known to authors after the area chair meeting and before the ICCV 2013 deadline.</li>
                  <li><b>Apr 16, 2013 - Camera-ready Deadline</li>
