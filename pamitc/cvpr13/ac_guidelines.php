This page provides detailed guidelines to Area Chairs of CVPR 2013 as to how the process will unfold and what are their responsibilities.<br><br>

<span class="cvprparagraphheader">Role of the Area Chairs</span>
<br><br>
The ACs help identify suitable reviewers, assign reviewers to papers, and evaluate the reviews as well as the authors' responses. ACs are expected to play an active role in ensuring higher quality and more timely reviews, and in initiating discussions amongst reviewers (anonymously), and in looking at authors' responses to the reviews. At the AC meeting in February 2013, ACs will work with other ACs (in pairs and then in panels) to decide which papers to accept and will write consolidation reports to explain the decisions.
<br><br>

<br>
<span class="cvprparagraphheader">Best Practices of Being an Area Chair</span>
<br><br>

We want to remind all ACs of some of the basic (and obvious) practices of being an AC.
<ul>
<li>You have a strong influence on a decision on a paper. Take it seriously and be fair.</li>
<li>DO NOT talk to any other AC about papers assigned to you until you are told about who is on your panel and who your buddy is. There may be several other ACs conflicted with the paper.</li>
<li>The Program Co-Chairs did not submit any papers, so they are not in direct conflict with any papers and they know all authors of all papers and also all reviewers/ACs. They will NOT be involved with any decisions of papers from institutions with which they are in conflict. You can send them a question about any paper and if one of them is conflicted, another one will respond to you without including the conflicted PC Chair.</li>
<li>DO NOT talk to any other AC about your own paper (the paper you are an author on) or a paper you have some conflict with, during this whole process, unless that AC and you are already collaborators and both of you have an already defined conflict.</li>
<li>Be professional and willing to listen to other reviewers and ACs. Do not give in to undue influence from anyone.</li>
<li>Remember, it is not fair to dismiss any review without looking at other reviews and reading the paper yourself to make an evaluation. Outright rejecting a review is not acceptable without a clearly articulated supporting argument. Area chairs who wish to make a decision that is not clearly supported by the reviews will have to present the paper to their panel at the meeting.</li>
<li>Senior members of the committee should guide and advise the younger and first-time members. First-time members, please feel free to ask for help as needed.</li>
</ul>




<br>
<span class="cvprparagraphheader">Consolidation Reports</span>
<br><br>

Area Chair Consolidation Reports are the MOST CRUCIAL aspect of the review process of CVPR. This is where the Area Chair justifies his/her recommendation to accept/reject a paper. The Program Chairs will read all Consolidation Reports and we have committed ourselves to ensure that these reports are representative of a good quality review process. These reports should highlight why the decision was reached. If all reviewers agree on a paper, this consolidation report can be simple, but feel free to encourage authors and provide constructive feedback. If there is even a slight disagreement on the reviews, it is your job to clarify why and how the disagreement was resolved. Again, just saying "reviewer 1 was wrong" without any reasons is not acceptable. Explain why. For the most divergent reviews, explain the process you went through to reach a decision and if you and others read the paper, try to add some details from your and others reviews with the consolidation reports. Overruling all three reviewers to make a decision on a paper is not acceptable. If you strongly feel that the paper deserves that, make sure to discuss it in detail with your AC Buddy and also in your AC Panel. In such cases, (a) you should read the paper completely, (b) provide a detailed consolidation report, and (c) solicit confirmation of the decision from your panel. The program chairs will review and confirm or reverse any such decisions that do not have the consensus support of the panel.
<br><br>

<br>
<span class="cvprsectionheader">Area Chair Instructions</span>
<br><br>

During the reviewing phase, please periodically log in to the <a target="_blank" href="https://cmt.research.microsoft.com/CVPR2013/">CMT site</a> and monitor the review progress.
<br><br>

<span class="cvprparagraphheader">Phase 1 - Assigning Reviewers</span><br>
<br>

The Program Chairs are going to assign papers to you based on (a) papers from authors not in conflict with you (see conflict of interest in the <a href="reviewer_guidelines.php#conflict">Reviewer Instructions</a>), (b) subject areas you chose when you registered on the review site, (c) subject areas, the authors chose when they registered the paper, and (d) results from the “Toronto" document matching system. As can be imagined, this will <b>not</b> be perfect as sometimes the Program Chairs have to do some load-balancing. Area Chairs should look through all the papers assigned to them and ensure that they are (1) not conflicted and (2) are knowledgeable in the subject area.
<br><br>

For each paper you are assigned you must initially <b>select 7 or more candidate reviewers during the reviewer assignment step</b>. After all ACs have completed this step, the CMT system will then select 3 from each candidate list, subject to necessary constraints on reviewer load. If it is not able to assign 3 from your list, it will assign fewer than 3, and you will be required to iterate again suggesting additional reviewers. <b>CMT will not assign a reviewer to a paper that you have not suggested. Several iterations may be necessary if you only suggest the minimal number of candidate reviewers, and those will occur in a relatively short cycle (24 or 48 hours)</b>. If you plan to only assign the minimal number of reviewers, please expect to be available on short notice to add a few more to the papers which are not satisfied on the first round. If you will be unavailable for several days, it is better to assign 10 or more candidate reviewers. <b>Please assign as many candidate reviewers as possible to each paper, as long as you are comfortable getting any subset of 3 actually assigned</b>. You must assign at least 7, and the more you assign, the greater the chances you will not be required to suggest additional reviewer names for that paper. While CMT will not automatically do so, the program chairs may personally assign reviewers based on preference keyword match or any other scheme necessary to papers of ACs who do not respond in a timely manner.
<br><br>

Here are some suggestions on how to proceed. It can take anywhere from 4-6 minutes a paper do this, if you look at the paper and other info to help make a decision. Considering a max load of 35 papers, you can see that this may take some time. See <a href="reviewer_guidelines.php#instructions">Reviewer Instructions</a>  for details of how to use the system to assign reviewers.

<ul>

<li>You have been chosen as an Area Chair because of your specific expertise in certain areas of vision and also your general expertise in computer vision. When you look at a paper, you may just come up with a name or three of ideal reviewers. Consider these people as the reviewers for this, but do also consider things like, (a) are they current in the field and (b) will they do a good job in a timely manner.</li>

<li>Look at the paper, especially the Introduction, Related Work, and Citations at the end. See who they refer to and whose work they are building on. Often the cited people would be ideal reviewers.</li>

<li>Remember that the system will give you some recommendations, based on matching subject areas and document statistics. This may provide a good starting point, but will not be perfect.</li>

<li>Usually, we suggest that you choose a reviewer you know and trust. But the vision community is large. If the system recommends someone you do not know, look at their webpage or their papers on ACM or IEEE digital libraries or Google Scholar and the like.</li>

<li>Be sure to spend a decent amount of time doing this.  <b>The quality of the reviewer assignments has a tremendous impact on the quality of the reviews and the final decisions</b>.</li>

<li>You may find it helpful to maintain your own spreadsheet with your list of preferred reviewers for each paper and the final paper assignments.</li>

</ul>



<br>
<span class="cvprparagraphheader">Phase 2 - Check Incoming Reviews</span><br>
<br>

After you have ranked reviewers, the Program Chairs, with the review system's help will distribute papers to the reviewers. During this phase, we recommend that you log into the system to check if any reviews are being entered. Remember that the reviewers <b>do not know</b> your identity, so it is best if you do not contact them and/or identify yourself. This is yet another form of "anonymity" in the review process, as we don't want the reviewers to be unduly influenced by anyone. If reviews are showing up, look at them and see if (a) the reviewer has added sufficient detail and (b) has used appropriate tone in the review, and most importantly (c) provided a knowledgeable review. Contact the reviewers via the review system to get them to clarify issues from the review. Doing this during the process will reduce how much you need to do at the deadline when reviews are due.
<br><br>

After the review deadline has passed, check if all reviews are in. If not, send them a note and nicely <b>demand</b> that they finish the reviews ASAP. The system will also send reminders to them. 
<ul>
</li>

<li>
In "Area Chair" console, select "Consolidation Reports".
</li>

<li>
You should be able to see all the reviewers' names and the review status right next to each. For this incarnation of CMT, we just highlight cases where reviews have not been submitted (in red).
</li>

<li>
For each paper, you can:
<ul>
<li>View all the reviews ("View All").</li>
<li>Email the reviewers anonymously via CMT ("Email Reviewers"). You can select all the assigned reviewers for that paper (default), or just those not done with reviewing, or specific reviewer(s). For the last item, you will be able to edit the recipient list. Remember: Don't identify yourself or the other reviewers in the email.</li>
</ul>
</li>

<li>
You can also choose to bulk email all reviewers or all reviewers who have not completed their reviews (for all the papers in your stack). See below the title "Consolidation Reports" ("Email Reviewer"). Mouse over the "Email Reviewer" box and you will see the two options (mentioned earlier) from which to choose.
</li>

<li>
You can view all reviews for all papers in a single webpage. Just select "View All Reviews" in the line right below the title "Consolidation Reports".
</li>

<li>
<b>Important note on emailing via CMT</b>: Since any email sent on your behalf is anonymously sent through CMT, the communication is <b>one way only</b>. Please tell your email recipients not to respond to the email, because there is no automatic routing of email to you. So, be very specific about what you would like the reviewer(s) to do, e.g., improve their review in a certain way.
</li>

<li>
It is the area chair's responsibility to contact and urge the reviewers to finish in a timely manner.
</ul>



<br>
<span class="cvprparagraphheader"><a name="discussion"></a>Phase 3 - Discussion Phase</span><br>
<br>

Right after the review deadline, please look through ALL the reviews.. If any review appears unreasonable (e.g., one-liner comments or overly harsh), please contact the reviewer and ask for clarification. Remember that communication is only one-way, so be specific about what you need from the reviewer. Check again in one or two days to see if there is any update to that review. Since we will not be keeping track of your emails, so please do the book-keeping yourself (best to make a note of what you've done).  Again, you may find it helpful to create and maintain your own spreadsheet to keep track of the reviews, scores, and any high-level discussion items or to-do lists.
<br><br>

<span class="cvprparagraphheader">3.1. When to Initiate a Discussion</span>
<br><br>

Please start a discussion any time you feel that the overall scores and/or the comments are insufficient to allow you to make a decision (e.g., all "Borderlines"). <b>At the very least</b>, initiate a discussion if the difference between the maximum and minimum scores is greater than 2 levels.
<br><br>

<span class="cvprparagraphheader">3.2. During the Discussion</span>
<br><br>

You play the role of a moderator in the discussion. While it is ideal to reach a consensus, don't feel obligated to force one - the discussions are mainly to allow you to make more informed decisions. It is ok if the reviewers end up not changing their overall scores as long as you've gained a better understanding of how they arrived at their scores. Stick to the facts, and do not influence the outcome of the discussion by imposing your views on the paper. However, if you disagree with the majority view, say so, provide compelling reasons, and solicit feedback from the reviewers. If the discussion looks like it is getting out of hand (e.g., when a reviewer makes inappropriate remarks or a battle of words erupts), please contact us (<a href="http://www.google.com/recaptcha/mailhide/d?k=01s__rgJSPZBirGZ4V81uanw==&amp;c=ts6rdTi13FCpxaJ0A3woJPPUUgA_eE8ek0_whlYVRlM=" onclick="window.open('http://www.google.com/recaptcha/mailhide/d?k\07501s__rgJSPZBirGZ4V81uanw\75\75\46c\75ts6rdTi13FCpxaJ0A3woJPPUUgA_eE8ek0_whlYVRlM\075', '', 'toolbar=0,scrollbars=0,location=0,statusbar=0,menubar=0,resizable=0,width=500,height=300'); return false;" title="Reveal this e-mail address">cvpr...@gmail.com</a>) immediately. Don't forget to let us know the paper number. For cases of "Borderlines", if the discussion appears to not to be heading towards any decision, we suggest you ask the question "If you had to choose now, would you accept or reject this paper?"
<br><br>

<span class="cvprparagraphheader">3.3. Anonymous Discussion Feature</span>
<br><br>


This feature can be accessed from the "Detailed Meta-Reviews and Discussions" page: 
<ul>

<li><span class="boldred">You must do the following steps to enable the reviewers to participate in the discussion. Emailing the reviewers via CMT is NOT the right way to initiate the discussion.</span></li>

<li>To initiate a discussion for a specific paper, first enable the discussion and then select "View/Post Message" under the "Discussion" column for that paper.</li>

<li>In the "Paper Discussion" page, click on "Start A New Topic" to initiate the discussion (unless a reviewer took the initiative of starting one, in which case you reply; see the next bullet item). We suggest that your first post starts with a very brief summary of the reviews, a request to look at the other reviews for details, followed by specific things you want the reviewers to address.</li>

<li>Once a discussion has been initiated, click on "Reply" on the far right to continue posting on the same discussion thread.</li>

<li>Please sign your posts as "Area Chair", and identify the reviewers by the review number. <b>Never identify yourself or other reviewers by name</b>.</li>

<li>Anytime a post is made (either by the area chair or a reviewer), the area chair and reviewers will receive an email notification from CMT with the subject that looks like "CVPR2013: New reviewer discussion posted for Paper ID XXX". There is a link in the email you can use to join the discussion (after logging in, you will be routed directly to the discussion page). Alternatively, you can just log in to CMT as usual.</li>

<li>After you've posted, <b>do not refresh the page</b>(e.g., by hitting F5)! This will generate another post with the exact same message!</li>

<li>In the discussion, you (as AC) can see the identities of the posters. However, each reviewer will NOT know the identities of the other posters.</li>

<li>Once the author rebuttal period is over, you and the reviewers will be able to see the author rebuttal.</li>

<li>Because of the frank nature of the discussions, the authors will not see them at any time. The consolidation reports will be visible to authors only after the AC meeting, when decisions are made known.</li>

<li>Reviewer instructions for the discussion feature are given <a href="reviewer_guidelines.php#ac_discuss">here</a> . Note that reviewers can revise their reviews until the review deadline.</li>

</ul>







