<span class="cvprparagraphheader">Phase 4 - Rebuttal Period</span><br>
<br>

During the author rebuttal period reviewers are no longer allowed to modify their reviews.
<br><br>

<br>
<span class="cvprparagraphheader">Phase 5 - Review Author Feedback and Discuss with Reviewers</span><br>
<br>

You should be able to see the Author Rebuttal/Feedback to the reviews. .You can start a discussion using the discussion board (see Phase 3: <a href="#discussion">Discussion Phase</a>) with the reviewers of the papers, especially if the rebuttal is responding to a specific review. While, the reviews cannot be changed, you can take the input from the reviewers at this phase to help in your proposed decision and the consolidation report that you have drafted (or will!). Make sure to be clear about how the rebuttal was taken into account in the consolidation report. Remember, all consolidation reports are considered a draft and all decisions are just proposed and will be finalized at the AC Meeting.
<br><br>

