<b>Once you've been notified that the papers have been assigned to you,
please log in to the CMT site and follow these steps:</b>
<br><br>

<span class="cvprparagraphheader">1. Download papers and check for
possible conflict or submission rule violations:</span>
<ul class="body_ul">
<li>In the Section "Paper Reviews and Discussions", click on "Download
Assigned Papers". This allows you to download a zip file containing
all the papers plus supplementary files (if available).</li>
<li><b><u>Contact the Program Chairs</b></u> immediately if:
  <ol>
   <li>You think you are in any way conflicted with the paper (see
   above).</li>
   <li>There is a violation of the stated paper submission rules.</li>
   <li>Such a violation includes:
     <ul class="body_ul">
       <li>Over 8 pages (not including references),</li>
       <li>Constitutes a double submission,</li>
       <li>Supplementary material includes a newer version of the
       paper.</li>
       <li><b>Please specify the exact nature of the violation.</b></li>
   </ul></li>
</ol></li>
<li>For your reference, the paper submission guidelines, which
includes descriptions of CVPR 2015's dual submission policy, can be
found in the <a href="author_guidelines.php">Author
Guidelines</a>.</li>
</ul>

<span class="cvprparagraphheader">2. Familiarize yourself with the data under "Detailed Reviews and Discussions":</span>
<ul class="body_ul">
<li>"Paper Summary" label: next to it, you'll see the icons "+" and
"-". Clicking on "+" shows you all the abstracts; clicking on "-"
collapses all them back.</li>
<li>At the end of each paper title, you'll see "+" as well. This has
the same function of showing the abstract for that paper, toggling to
"-" at the same time, which collapses it when selected.</li>
<li>Please take the time to familiarize yourself with the table
entries; clicking on any of the column heading (e.g., "Paper ID")
sorts according to its description.</li>  
</ul>

<span class="cvprparagraphheader">3. Review papers and assign them a
Preliminary Rating:</span>
<ul class="body_ul">
<li>For a paper, under the review column, click "Add" (to the right of
the "Review" line) to enter a review. Please see
the <a href="#reviewer_guidelines">Reviewer Guidelines</a> above and
take each review seriously.  The review form contains detailed
instructions about writing the review and the meaning of the different
ratings (Oral, Poster, etc.).</li>
<li>CMT does not allow users to type certain characters into a text
box that could be interpreted as html tags (for example, "y&lt;x") or a
malicious script. As a workaround, introducing spaces between these
characters (for example, "y &lt; x") will allow you to submit the text
since this can no longer be interpreted as an html tag.</li>
<li>If you save your review as a draft, it is visible only to you. You
can access your draft review form by clicking on the same "Add"
link. To make the review visible to the Area Chair, click on the
"Submit" button in the review form. "Submit" won't work if any of the
required items is not filled.</li>
</ul>

<span class="cvprparagraphheader">4. (Optional) Review papers
offline:</span>
<ul class="body_ul">
<li>You have two options to access the "Offline Reviewing" page: (1)
In the "Paper Reviews and Discussions" page, click on "Review papers
offline" link near the top of the page, or (2) In the "View/Edit
Review" page, click on "offline reviewing" link.</li>
<li>In the "Offline Reviewing" page, you can download one review
template file for a single paper, several papers, or all the
papers. We suggest that you download a review template file for each
paper to avoid confusion.</li>
<li>Please read instructions on how to modify the file to incorporate
your responses. Note that you must not add certain characters in your
responses that could be interpreted as html tags or a malicious
script. See item 3 above.</li>
<li>You can upload the completed file using the "Upload" interface at
the bottom of the page. The new uploaded version will (destructively)
overwrite the current review.</li>
<li>We suggest that you try downloading a review template file for one
paper, enter test responses, and upload to get a sense of how it
works.</li>
<li>You should always verify the review after uploading (by inspecting
it online).</li>
<li>We suggest that you use an XML editor to edit the file, for
example: <u>EditiX</u> (Windows, Unix/Linux, Mac OS X) or <u>XML
Notepad 2007</u> (Windows only). (Remember to edit only fields
currently filled with the phrase "REPLACE THIS WITH YOUR
ANSWER".)</li>
</ul>

<span class="cvprparagraphheader">5. Participate in discussions with
Area Chairs and other reviewers:</span>
<ul class="body_ul">
<li>After the rebuttal period, reviewers will work with Area Chairs to
clear up any confusions and attempt to reach consensus on papers.</li>
<li>The CMT site has an electronic bulletin board feature that allows
Area Chairs to contact reviewers anonymously. Once the Area Chair
posts a note, reviewers will be notified and asked to log in to see
the post and respond. The identities of the reviewers will be hidden
from each other.</li>
</ul>


<span class="cvprparagraphheader">6. Enter your Final Recommendation</span>
<ul class="body_ul">
  <li>After the rebuttal period you will enter your final
  recommendation on CMT.  This may differ from your preliminary
  rating, and should reflect your judgment taking into account all the
  other reviews, the authors' rebuttal, and the discussion about the
  paper (if any).</li>
</ul>


