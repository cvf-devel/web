<p><b>How to use the collated abstracts:</b></p>

<p>The collated abstracts are designed to be used with each poster
session.  In particular, the page number of each abstract corresponds
to the poster number in the given session. To use these abstracts to
preview a poster session, we recommend the following:</p>

<p>1) Obtain the collated abstracts for the poster session of
interest, either by downloading it above, or obtaining it from the USB
drive distributed at registration.</p>

<p>2) Open the collated set in a PDF viewer.</p>

<p>3) Set the PDF viewer so that it shows a single page rather than a scrolling view or a multipage view.</p>

<p>4a) Use <a href="program.php">main program page</a> as a table of contents for each collated set of abstracts. Go to the page number corresponding to the abstract of interest (i.e., page 37 for paper #37, as shown on the main program page).</p>

<p>4b) Alternatively, simply search for the title or subject of the paper of interest, and go directly to that paper, using your PDF viewer's search capability.</p>


