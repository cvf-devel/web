<span id="exhibitor" class="cvprsectionheader">Exhibitor Information</span><br />
 
<p>CVPR has a strong tradition of hosting exhibits of advanced
computer vision technology, products, and publications. Our exhibit
spaces are strategically-placed to foster interaction between the
exhibitors and conference attendees, ensuring maximum exposure and
interaction. We particularly encourage companies and other commercial
entities or laboratories to participate in showcasing their
products.</p>
 
<p>The main difference between the demos and exhibits categories is
that demos are usually related to specific research papers (not
necessarily presented at this CVPR), while exhibits are related to
products.</p>
 
<p>The exhibitors must agree to the terms and conditions in the
  <a href="files/Exhibitor_Prospectus.pdf">Exhibitor Prospectus</a> during
  the <a href="http://www.cvent.com/d/6rq0zv">registration process</a>. All
registrations are made online and payment is by check or credit
card. Additional items are also ordered during the online
registration. The deadline for exhibit applications is April 15, 2015.</p>
 
