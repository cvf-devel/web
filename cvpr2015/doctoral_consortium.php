<span class="cvprpageheader">Call for Participation: CVPR 2015 Doctoral Consortium</span><br /><br />

<span class="cvprsectionheader">Overview</span>

<p>The CVPR Doctoral Consortium will provide a unique opportunity to
students who are close to finishing, or who have very recently
finished, their doctorate degree, to interact with renowned and
experienced researchers in the field, and discuss with them their
ongoing research and career plans. One senior member of the community
will be assigned as mentor for each student based on similarity of
research interests. At the meeting, each student will present a poster
either overviewing his or her thesis research or a single recent
paper, which will be discussed with the mentors and other
participants.</p>

<span class="cvprsectionheader">Eligibility</span>

<p>All Ph.D. students in computer vision who will graduate within the
next 6 months, as well as those who have graduated in the past 6
months are eligible.</p>

<span class="cvprsectionheader">Submission Guidelines</span>

<p>Eligible students who would like to participate in the Doctoral
Consortium should submit an application via CMT at <a
href="https://cmt.research.microsoft.com/DC2015/">https://cmt.research.microsoft.com/DC2015/</a>. The
application is due on March 16th, and should include the
following. Items 1-3 should be combined into a single pdf file. Item 4
will be uploaded as required supplemental material.</p>

<ol>
  <li>a two-page cover letter summarizing the applicant's research and progress to date and also proposing names of potential mentors,</li>
  <li>a CV including the expected graduation date,</li>
  <li>the title and author list of the poster that will be presented at the consortium, which may or may not be presented at CVPR as well,</li>
  <li>a note from their advisor confirming the date of graduation and stating his/her availability as a mentor</li>
</ol>


<span class="cvprsectionheader">Review Process</span>

<p>If all applications cannot be supported, selection will be based on
the above material and the student's graduation date. Institutional
representation will be taken into account; it is unlikely that more
than two students from the same institution will be invited.</p>

<span class="cvprsectionheader">Travel Awards</span>

<p>Selected students will receive travel reimbursements for part of
their expenses. The amount is anticipated to be on the order of $700
per participant.</p>

