CVPR is one of the premier Computer Vision events comprising a main
conference and numerous co-located workshops and tutorials. CVPR 2014
will continue the successful tradition of allowing researchers to
present live demonstrations of their work.<br /><br />

Persons interested in the demo program at CVPR 2014 should submit a
demonstration application form (see above) by May 9, 2014.<br /><br />

Note that demos do not need to be limited to only papers that are
presented at the main conference or associated workshops. All
contributions relevant to the computer vision community are
welcomed.<br /><br />

Please be advised that all of the presenters for accepted demos must
be registered at the conference.  Demos will be selected based on
their appropriateness for CVPR.  If there are more applications than
the available space allows, the time of submission of the demo
application form will be considered in the selection (with earlier
submissions having precedence).<br /><br />

Please note that demos by persons representing commercial products are
welcome, but should be part of the CVPR exhibitions, rather than
demonstrations.  For more details on exhibitions, please see <a
