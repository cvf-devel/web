<span class="cvprparagraphheader">Oral sessions:</span>
<ul>

<li><b>Oral presenters</b> should be present at the podium (in the correct room) at least 10 minutes before the start of the session and should introduce themselves to the session chairs as soon as they arrive. You can check your session <a href="program.php">here</a>.</li>

<li><b>Your talk may be recorded.</b> Please note the <span class="boldred"><b>June 13th</b></span> deadline for submitting your Release Form, see recording <a href="#recording_details">details below</a>.</li>

<li>If you require any special assistance during your talk or to get to the podium, please let the session chair know of this before the session or email the <a href="http://www.google.com/recaptcha/mailhide/d?k=01Vgzw2i8zjYktIZfbedK9FQ==&c=NaKH3dNkw3Fz9REUfzORHAwKkT1RU5RzT15Q7DEQgURtf0BMD9OrGkNCfxX_RFQG" target="_blank">program chairs</a>.</li>

<li>Oral speakers should test their own laptops with the projector before the session, to avoid any technical difficulties.</li>

<li>Oral talks are given 12 minutes plus 3 minutes for questions. You will be asked to leave the podium once your time is up, please make sure you do not exceed the given time limit.</li>

<li>Each oral presenter can additionally present their work as a poster. Your poster is scheduled for the first poster session following the oral session of your paper. During the poster session a poster board will be provided for you. This is optional but a good opportunity to expose the work further and get extra feedback from the CVPR attendees.</li>

<li>In addition, each oral presenter can submit a one-minute video as detailed below.</li>

</ul>


<br/>
<span class="cvprparagraphheader">Poster sessions:</span>
<ul>

<li>Each poster is assigned a specific board identified by paper id as specified in the <a href="files/CVPR_2014_Pocket_Guide_Final.pdf" target="_blank">pocket guide</a>. Check with the volunteers or the registration desk if you cannot find your poster board.</li>

<li>The physical dimensions of the poster boards that will be available this year are 8 feet wide by 4 feet high. Please review the <a href="files/cvpr14-poster-template-n.ppt">CVPR14 poster template</a> for more details on how to prepare your poster. You do not need to use this template, but please read the instructions carefully and prepare your posters accordingly.

<li>This year's conference will not feature spotlight presentations. Instead, you'll be given the opportunity to submit a one-minute video (see <a href="video_spotlights.php">Video Spotlights</a>). Video presentations will be available to attendees before, during and after the conference, see details below.</li>



</ul>


