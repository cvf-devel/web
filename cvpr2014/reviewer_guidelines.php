<span class="cvprparagraphheader">5. CMT Review Form</span><br /><br />

<b>Paper Summary.</b> Please summarize in your own words what the paper is about.<br /><br />

<b>Paper Strengths.</b> Please discuss the positive aspects of the paper. Be sure to comment on the paper's novelty, technical correctness, clarity and experimental evaluation. Notice that different papers may need different levels of evaluation: a theoretical paper may need no experiments, while a paper presenting a new approach to a known problem may require thorough comparisons to existing methods. Also, please make sure to justify your comments in great detail. For example, if you think the paper is novel, not only say so, but also explain in detail why you think this is the case.<br /><br />

<b>Paper Weaknesses.</b> Please discuss the negative aspects of the paper: lack of novelty, technical errors, lack of clarity, insufficient experimental evaluation, etc. Please justify your comments in great detail. If you think the paper is not novel, explain why and give a reference to prior work. Do not ask the authors to cite your own work. If you think this is essential, write it in the confidential comments. The AC will pass it to the authors if appropriate. If you think there is an error in the paper, provide details of where the error is and why it is an error. If you think the experimental evaluation is insufficient, remember that theoretical results/ideas are essential to the CVPR community and that a theoretical paper need not have experiments. It is not appropriate to reject a paper simply because it did not outperform other existing algorithms, especially if the theory is novel, interesting or presents some value to the community. It is also not okay to ask for comparisons with unpublished papers and papers published after the CVPR deadline. Last but not least, remember to be polite and constructive in your comments.<br /><br />

<b>Preliminary Rating.</b> Please rate the paper according to the following choices.<br /><br />

<div style="margin-left: 1em;">

<b>Oral:</b> these are papers whose quality is in the top 10% of the papers at CVPR. Examples include a theoretical breakthrough with no experiments; an interesting solution to a new problem; a novel solution to an existing problem with solid experiments; or an incremental paper that leads to dramatic improvements in performance.<br /><br />

<b>Oral/Poster:</b> these are very strong papers, which may have one weakness that makes you unsure as to whether they should be oral or poster.<br /><br />

<b>Poster:</b> these are strong papers, which have more than one weakness. For example, a well-written paper with solid experiments, but incremental; a paper on a well-studied problem with solid theory, but weak experiments; or a novel paper with good experiments, but poorly written.<br /><br />

<b>Weak Reject:</b> these are papers that have some promise, but they would be better off by being revised and resubmitted.<br /><br />

<b>Strong Reject:</b> these are papers that have a major flaw, or have been done before.<br /><br />

</div>

<b>Preliminary Evaluation.</b> Please indicate to the AC, your fellow reviewers, and the authors your current opinion on the paper. Please summarize the key things you would like the authors to include in their rebuttals to facilitate your decision making. There is no need to summarize the paper or reviews. If you have additional concerns that were not included in the reviews, please be sure and include them as well.<br /><br />

<b>Confidence.</b> Write "Very Confident" to stress that you are absolutely sure about your conclusions (e.g., you are an expert who works in the paper's area), "Confident" to stress that you are mostly sure about your conclusions (e.g., you are not an expert but can distinguish good work from bad work in that area), and "Not Confident" to stress that that you feel some doubt about your conclusions. In the latter case, please provide details.<br /><br />

<b>Confidential Comments to the PC/AC Chairs (will not be seen by authors)</b><br /><br />

<b>Reviewed by.</b> The assigned reviewer must complete his review form and write his/here name here. Under very unusual circumstances, someone else designated by the assigned reviewer might do the review. In that case, please write the name of the alternate reviewer and explain why the review was reassigned. In either case, by submitting this form, the assigned reviewer states that he/she agrees with the content of the review and is responsible for it.<br /><br />

The following questions should be answered <u>after the rebuttal</u>.<br /><br />

<b>Final Rating.</b> After reading the author's rebuttal, please rate the paper according to the following choices.<br /><br />

<b>Final Recommendation.</b> After reading the author's rebuttal and the discussion, please explain your final recommendation. Your explanation will be of highest importance for making acceptance decisions and for deciding between posters and orals. Include suggestions for improvement and publication alternatives, if appropriate.<br /><br />


