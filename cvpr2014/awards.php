<span class="cvprsectionheader">Main Conference Awards</span>
<br/><br/>
At the AC meeting, the AC panels selected a list of outstanding papers for consideration for an award. The 10 top papers from this list were sent to an independent Awards Committee, which was comprised by people that had not participated in the review process of these papers, had no conflict of interest with any of the authors and institutions of the authors of these papers, and were experts in the areas of the top 10 papers. The Awards Committee selected three awards. 
<br/><br/>
<b>Awards Committee:</b> Stan Sclaroff (Chair), Alan Yuille, Guillermo Sapiro, Serge Belongie, Todd Zickler.
